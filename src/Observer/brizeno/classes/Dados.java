package Observer.brizeno.classes;

public class Dados {
	
	public Dados(int valorA, int valorB, int valorC) {
		this.valorA= valorA;
		this.valorB = valorB;
		this.valorC= valorC;
	}

	public int getValorA() {
		return valorA;
	}
	public int getValorB() {
		return valorB;
	}
	public int getValorC() {
		return valorC;
	}



	private int valorA;
	private int valorB;
	private int valorC;
}
